// Export options
export interface ExportOptions {
  format: 'html' | 'zip' | 'json';
  includeAssets: boolean;
  minify: boolean;
  inlineStyles: boolean;
  generateSitemap: boolean;
  generateRobots: boolean;
  basePath: string;
  prettyUrls: boolean;
  outputDir: string;
}

// Export result
export interface ExportResult {
  success: boolean;
  files: GeneratedFile[];
  errors: string[];
  warnings: string[];
  totalSize: number;
  duration: number;
}

// Generated file
export interface GeneratedFile {
  path: string;
  content: string | Blob;
  size: number;
  type: 'html' | 'css' | 'js' | 'json' | 'asset' | 'other';
}

// Default export options
const DEFAULT_EXPORT_OPTIONS: ExportOptions = {
  format: 'zip',
  includeAssets: true,
  minify: false,
  inlineStyles: true,
  generateSitemap: true,
  generateRobots: true,
  basePath: '/',
  prettyUrls: true,
  outputDir: 'dist',
};

// Generate HTML for a page
const generatePageHTML = (
  blocks: Block[],
  globalStyles: GlobalStyles,
  options: ExportOptions,
  page: { title: string; slug: string; seoTitle?: string; seoDescription?: string }
): string => {
  // Generate CSS variables from global styles
  const cssVariables = `
    :root {
      --color-primary: ${globalStyles.primaryColor};
      --color-secondary: ${globalStyles.secondaryColor};
      --color-accent: ${globalStyles.accentColor};
      --color-background: ${globalStyles.backgroundColor};
      --color-text: ${globalStyles.textColor};
      --font-body: ${globalStyles.fontFamily};
      --font-heading: ${globalStyles.headingFont};
      --radius: var(--radius-${globalStyles.borderRadius});
      --button-style: ${globalStyles.buttonStyle};
    }
  `;

  // Generate block HTML
  const blocksHTML = blocks.map(block => {
    const definition = BLOCK_DEFINITIONS[block.type];
    if (!definition) return '';

    // Generate block HTML based on type
    return generateBlockHTML(block, globalStyles);
  }).join('\n');

  // Generate full HTML document
  const html = `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${page.seoTitle || page.title}</title>
  ${page.seoDescription ? `<meta name="description" content="${page.seoDescription}">` : ''}
  <meta name="generator" content="Omugwo Academy Site Builder">
  
  <!-- Tailwind CSS -->
  <script src="https://cdn.tailwindcss.com"></script>
  <script>
    tailwind.config = {
      theme: {
        extend: {
          colors: {
            primary: '${globalStyles.primaryColor}',
            secondary: '${globalStyles.secondaryColor}',
            accent: '${globalStyles.accentColor}',
          },
          fontFamily: {
            body: '${globalStyles.fontFamily}',
            heading: '${globalStyles.headingFont}',
          }
        }
      }
    }
  </script>
  
  ${options.inlineStyles ? `<style>${cssVariables}</style>` : '<link rel="stylesheet" href="/styles.css">'}
  
  <!-- Google Fonts -->
  <link rel="preconnect" href="https://fonts.googleapis.com">
  <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin>
  <link href="https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700&display=swap" rel="stylesheet">
</head>
<body class="font-body bg-background text-text antialiased">
  ${blocksHTML}
  
  ${options.minify ? '' : '<!-- Generated by Omugwo Academy Site Builder -->'}
</body>
</html>`;

  return options.minify ? minifyHTML(html) : html;
};

