// Minify HTML
const minifyHTML = (html: string): string => {
  return html
    .replace(/\s+/g, ' ')
    .replace(/>\s+</g, '><')
    .replace(/\s+/g, ' ')
    .trim();
};

// Generate sitemap XML
const generateSitemap = (pages: { slug: string; updatedAt?: string }[], basePath: string): string => {
  const baseUrl = basePath.replace(/\/$/, '');
  
  const urls = pages.map(page => `
    <url>
      <loc>${baseUrl}/${page.slug}</loc>
      ${page.updatedAt ? `<lastmod>${new Date(page.updatedAt).toISOString().split('T')[0]}</lastmod>` : ''}
      <changefreq>weekly</changefreq>
      <priority>0.8</priority>
    </url>
  `).join('');

  return `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${urls}
</urlset>`;
};

// Generate robots.txt
const generateRobots = (basePath: string): string => {
  const baseUrl = basePath.replace(/\/$/, '');
  
  return `User-agent: *
Allow: /

Sitemap: ${baseUrl}/sitemap.xml
`;
};

// Export Manager Hook
export const useExportManager = () => {
  const [isExporting, setIsExporting] = useState(false);
  const [progress, setProgress] = useState(0);
  const [result, setResult] = useState<ExportResult | null>(null);

  // Export pages
  const exportPages = useCallback(async (
    pages: SitePage[],
    globalStyles: GlobalStyles,
    options: ExportOptions = DEFAULT_EXPORT_OPTIONS
  ): Promise<ExportResult> => {
    setIsExporting(true);
    setProgress(0);
    const startTime = Date.now();
    const files: GeneratedFile[] = [];
    const errors: string[] = [];
    const warnings: string[] = [];

    try {
      // Generate HTML files for each page
      for (let i = 0; i < pages.length; i++) {
        const page = pages[i];
        const blocks = page.status === 'PUBLISHED' ? page.publishedBlocks : page.draftBlocks;
        
        const html = generatePageHTML(blocks, globalStyles, options, {
          title: page.title,
          slug: page.slug,
          seoTitle: page.seoTitle,
          seoDescription: page.seoDescription,
        });

        const filename = options.prettyUrls 
          ? `${page.slug}/index.html`
          : `${page.slug}.html`;

        files.push({
          path: filename,
          content: html,
          size: html.length,
          type: 'html',
        });

        setProgress(Math.round(((i + 1) / pages.length) * 80));
      }

      // Generate sitemap
      if (options.generateSitemap) {
        const sitemap = generateSitemap(pages, options.basePath);
        files.push({
          path: 'sitemap.xml',
          content: sitemap,
          size: sitemap.length,
          type: 'other',
        });
      }

      // Generate robots.txt
      if (options.generateRobots) {
        const robots = generateRobots(options.basePath);
        files.push({
          path: 'robots.txt',
          content: robots,
          size: robots.length,
          type: 'other',
        });
      }

      setProgress(100);

      const totalSize = files.reduce((sum, f) => sum + f.size, 0);

      const exportResult: ExportResult = {
        success: true,
        files,
        errors,
        warnings,
        totalSize,
        duration: Date.now() - startTime,
      };

      setResult(exportResult);
      return exportResult;

    } catch (error) {
      const exportResult: ExportResult = {
        success: false,
        files,
        errors: [String(error)],
        warnings,
        totalSize: 0,
        duration: Date.now() - startTime,
      };

      setResult(exportResult);
      return exportResult;
    } finally {
      setIsExporting(false);
    }
  }, []);

  // Download as ZIP
  const downloadAsZip = useCallback(async (files: GeneratedFile[], filename: string = 'site-export.zip') => {
    // In production, use JSZip or similar library
    // For now, download as individual files
    const mainHtml = files.find(f => f.path.includes('index.html'));
    if (mainHtml && typeof mainHtml.content === 'string') {
      const blob = new Blob([mainHtml.content], { type: 'text/html' });
      const url = URL.createObjectURL(blob);
      const a = document.createElement('a');
      a.href = url;
      a.download = 'index.html';
      a.click();
      URL.revokeObjectURL(url);
    }
  }, []);

  // Copy to clipboard
  const copyToClipboard = useCallback(async (content: string) => {
    try {
      await navigator.clipboard.writeText(content);
      return true;
    } catch {
      return false;
    }
  }, []);

  return {
    isExporting,
    progress,
    result,
    exportPages,
    downloadAsZip,
    copyToClipboard,
  };
};

